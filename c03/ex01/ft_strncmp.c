#include <unistd.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				result;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i] && i < n - 1)
		i++;
	result = s1[i] - s2[i];
	if (result < 0)
		return (-1);
	else if (result > 0)
		return (1);
	else
		return (0);
}
